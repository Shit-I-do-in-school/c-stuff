using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator_app
{
    internal class calculate
    {
        public double plusa(double tal1, double tal2)
        {
            return tal1 + tal2;
        }
        public double minusa(double tal1, double tal2)
        {
            return tal1 - tal2;
        }
        public double gångra(double tal1, double tal2)
        {
            return tal1 * tal2;
        }
        public double dela(double tal1, double tal2)
        {
            return tal1 / tal2;
        }
    }
}
